package PotRoastRecipe;


public class Main {

    
    public static void main(String[] args) {
displayPotRoastRecipe();
    }

    public static void displayPotRoastRecipe() {
        System.out.println("Pot Roast Recipe");
        System.out.println("================");

        System.out.println("Ingredients:");
        System.out.println("- 3 to 4 pounds of beef chuck roast");
        System.out.println("- 1 cup of beef broth");
        System.out.println("- 1 onion, chopped");
        System.out.println("- 3 carrots, peeled and chopped");
        System.out.println("- 3 potatoes, peeled and diced");
        System.out.println("- 2 cloves of garlic, minced");
        System.out.println("- Salt and pepper to taste");
        System.out.println("- 2 tablespoons of olive oil");
        System.out.println("- 2 teaspoons of dried thyme");
        System.out.println("- 2 teaspoons of dried rosemary");

        System.out.println("\nInstructions:");
        System.out.println("1. Preheat the oven to 325°F (163°C).");
        System.out.println("2. Season the chuck roast with salt and pepper.");
        System.out.println("3. In a large Dutch oven, heat olive oil over medium-high heat.");
        System.out.println("4. Sear the roast on all sides until browned.");
        System.out.println("5. Remove the roast and set it aside.");
        System.out.println("6. In the same pot, sauté the chopped onion and garlic until softened.");
        System.out.println("7. Add the beef broth, thyme, and rosemary. Bring to a simmer.");
        System.out.println("8. Place the seared roast back into the pot.");
        System.out.println("9. Add chopped carrots and diced potatoes around the roast.");
        System.out.println("10. Cover the pot and transfer it to the preheated oven.");
        System.out.println("11. Roast for about 3 hours or until the meat is fork-tender.");

        System.out.println("\nEnjoy your delicious pot roast!");
    }
}
